// Функция, которая выполняет все расчеты
function arrLength(arr) {
  const length = arr.length;

  // console.log(length);
  return length;
}

// Данные для задачи
const myArr = [3, 5, 7, 2, 4, 6, 1, 2];

// Вычисление результата
const myArrLength = arrLength(myArr);

// Вывод в консоль результата
console.log('Длина массива: ', myArrLength);

